let state = {
    topic: null,
    index: 0,
    score: 0,
    difficulty: 'easy',
    askedQuestions: []
};


function init() {
    const grid = document.getElementById('topic-grid');
    if (!grid) return;
    
    Object.keys(DB).forEach(topic => {
        const btn = document.createElement('button');
        btn.className = 'btn-topic';
        btn.innerText = topic.toUpperCase();
        btn.onclick = () => startQuiz(topic);
        grid.appendChild(btn);
    });
}


function startQuiz(topic) {
    state.topic = topic;
    state.index = 0;
    state.score = 0;
    state.difficulty = 'easy';
    state.askedQuestions = [];
    document.getElementById('home-screen').classList.add('hidden');
    document.getElementById('quiz-screen').classList.remove('hidden');
    renderQuestion();
}

/**
 * RENDER: Logic for picking random unasked question based on difficulty
 */
function renderQuestion() {
    // Filter unasked
    let unasked = DB[state.topic].filter(q => !state.askedQuestions.includes(q.q));
    
    // Attempt to match difficulty
    let pool = unasked.filter(q => q.d === state.difficulty);
    
    // Fallback if difficulty pool is empty
    if(pool.length === 0) pool = unasked; 
    
    // Final exit if total pool is empty
    if(pool.length === 0) {
        showResults();
        return;
    }

    const randomPick = Math.floor(Math.random() * pool.length);
    const qData = pool[randomPick];
    
    state.askedQuestions.push(qData.q);
    
    // Update UI Elements
    document.getElementById('q-current').innerText = state.index + 1;
    document.getElementById('progress-fill').style.width = `${(state.index + 1) * 10}%`;
    
    const tag = document.getElementById('difficulty-tag');
    tag.innerText = state.difficulty.toUpperCase();
    tag.className = `tag-${state.difficulty}`;

    document.getElementById('question-text').innerText = qData.q;
    const optGrid = document.getElementById('options-grid');
    optGrid.innerHTML = '';

    qData.a.forEach((opt, i) => {
        const btn = document.createElement('button');
        btn.className = 'btn-option';
        btn.innerText = opt;
        btn.onclick = () => handleAnswer(i, qData, btn, optGrid);
        optGrid.appendChild(btn);
    });
}

/**
 * LOGIC: Feedback, Score, and Latency
 */
function handleAnswer(choice, qData, clickedBtn, optGrid) {
    const allBtns = optGrid.querySelectorAll('.btn-option');
    allBtns.forEach(b => b.disabled = true);

    const isCorrect = (choice === qData.c);

    if (isCorrect) {
        clickedBtn.classList.add('correct-answer');
        
        // Advanced Scoring: Weights by difficulty
        state.score += (state.difficulty === 'easy' ? 50 : state.difficulty === 'medium' ? 100 : 200);
        
        // Scaling UP
        if(state.difficulty === 'easy') state.difficulty = 'medium';
        else if(state.difficulty === 'medium') state.difficulty = 'hard';
        
    } else {
        clickedBtn.classList.add('wrong-answer');
        allBtns[qData.c].classList.add('correct-answer');
        
        // Scaling DOWN
        if(state.difficulty === 'hard') state.difficulty = 'medium';
        else if(state.difficulty === 'medium') state.difficulty = 'easy';
    }

    // LATENCY: 1.2s delay for visual feedback
    setTimeout(() => {
        state.index++;
        if(state.index < 10) {
            renderQuestion();
        } else {
            showResults();
        }
    }, 1200);
}

/**
 * RESULTS: Final Assessment
 */
function showResults() {
    document.getElementById('quiz-screen').classList.add('hidden');
    document.getElementById('result-screen').classList.remove('hidden');
    document.getElementById('final-score').innerText = state.score;

    let road = "";
    if (state.score >= 1200) road = "Expert. You mastered the hardest questions in this domain.";
    else if (state.score >= 700) road = "Proficient. You have a solid grasp of complex concepts.";
    else road = "Learner. Focus on foundational basics to improve.";
    
    document.getElementById('roadmap-text').innerText = road;
}

